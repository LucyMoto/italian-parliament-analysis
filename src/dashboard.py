# ── Load data ─────────────────────────────────────────────────

@st.cache_data
def load_graph():
    with open(OUTPUTS_DIR / "deputy_graph.json", "r", encoding="utf-8") as f:
        data = json.load(f)

    # Backward compatibility with older analysis output
    for node in data.get("nodes", []):
        if "voting_participation_pct" not in node:
            node["voting_participation_pct"] = node.get("attendance_pct", 0.0)

    for party in data.get("parties", []):
        if "avg_voting_participation" not in party:
            party["avg_voting_participation"] = party.get("avg_attendance", 0.0)

    return data


def save_graph_html(html_str: str, filename: str = "deputy_graph.html") -> str:
    """
    Save the generated graph HTML into the project's static folder.
    Streamlit serves ./static/* at app/static/* when static serving is enabled.
    """
    static_dir = Path("static")
    static_dir.mkdir(exist_ok=True)

    out_path = static_dir / filename
    out_path.write_text(html_str, encoding="utf-8")

    return f"static/{filename}"


