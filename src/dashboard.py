# ── Filter data ───────────────────────────────────────────────

def filter_nodes(nodes, groups, side):
    filtered = [n for n in nodes if n["group"] in groups]
    if side == "Coalition":
        filtered = [n for n in filtered if n["side"] == "coalition"]
    elif side == "Opposition":
        filtered = [n for n in filtered if n["side"] == "opposition"]
    return filtered


filtered_nodes = filter_nodes(nodes, selected_groups, side_filter)
filtered_ids = {n["id"] for n in filtered_nodes}

filtered_edges = [
    e for e in edges
    if e["source"] in filtered_ids
    and e["target"] in filtered_ids
    and e["weight"] >= min_sim
] if show_edges else []


